public class Truck {

    private int axles;

    private int weight;

    public Truck(int axles, int weight) {
        this.axles = axles;
        this.weight = weight;

    }

    public Truck() {

    }

    public int getAxles() {
        return axles;
    }

    public void setAxles(int axles) {
        this.axles = axles;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }
}
